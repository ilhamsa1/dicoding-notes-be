const { deleteNoteByIdHandler } = require('./api/notes/handler');
const { editNoteByIdHandler } = require('./api/notes/handler');
const { getNoteByIdHandler } = require('./api/notes/handler');
const { addNoteHandler, getAllNotesHandler } = require('./api/notes/handler');

const routes = [
  {
    method: 'POST',
    path: '/notes',
    handler: addNoteHandler,
  },
  {
    method: 'GET',
    path: '/notes',
    handler: getAllNotesHandler,
  },
  {
    method: 'GET',
    path: '/notes/{id}',
    handler: getNoteByIdHandler,
  },
  {
    method: 'PUT',
    path: '/notes/{id}',
    handler: editNoteByIdHandler,
  },
  {
    method: 'DELETE',
    path: '/notes/{id}',
    handler: deleteNoteByIdHandler,
  },
];

module.exports = routes;
